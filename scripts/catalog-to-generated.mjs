#!/usr/bin/env node
/**
 * Reads content/catalog.json and writes src/lib/data.generated.ts.
 * Enable with USE_CONTENT_CATALOG=true (e.g. Netlify env) before `next build`.
 * Use when content/catalog.json is your catalog source for static builds.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = path.join(__dirname, "..");
const CATALOG_PATH = path.join(PROJECT_ROOT, "content", "catalog.json");
const OUT_PATH = path.join(PROJECT_ROOT, "src", "lib", "data.generated.ts");

if (process.env.USE_CONTENT_CATALOG !== "true") {
  process.exit(0);
}

if (!fs.existsSync(CATALOG_PATH)) {
  console.error(
    "USE_CONTENT_CATALOG=true but content/catalog.json is missing. Copy content/catalog.example.json to content/catalog.json."
  );
  process.exit(1);
}

let raw;
try {
  raw = JSON.parse(fs.readFileSync(CATALOG_PATH, "utf8"));
} catch (e) {
  console.error("Invalid JSON in content/catalog.json:", e.message);
  process.exit(1);
}

let collections = raw.collections;
let products = raw.products;

if (!Array.isArray(collections) || !Array.isArray(products)) {
  console.error("content/catalog.json must have { collections: [], products: [] } arrays.");
  process.exit(1);
}

/** Decap list widgets often emit objects like { id: "slug" } instead of plain strings. */
function normalizeProductIds(ids) {
  if (!Array.isArray(ids)) return [];
  return ids
    .map((x) => {
      if (typeof x === "string") return x;
      if (x && typeof x === "object") {
        return x.id ?? x.item ?? x.productId ?? x.slug ?? "";
      }
      return "";
    })
    .filter(Boolean);
}

function normalizeGallery(imgs) {
  if (!Array.isArray(imgs) || imgs.length === 0) return undefined;
  const out = imgs
    .map((x) => {
      if (typeof x === "string") return x;
      if (x && typeof x === "object") return x.path ?? x.url ?? "";
      return "";
    })
    .filter(Boolean);
  return out.length ? out : undefined;
}

collections = collections.map((c) => ({
  ...c,
  productIds: normalizeProductIds(c.productIds),
}));

products = products.map((p) => {
  const images = normalizeGallery(p.images);
  const next = { ...p };
  if (images) next.images = images;
  else delete next.images;
  return next;
});

const content = `// Auto-generated by scripts/catalog-to-generated.mjs from content/catalog.json – do not edit by hand.

import type { Collection, Product } from "./data";

export const collections: Collection[] = ${JSON.stringify(collections, null, 2)};

export const products: Product[] = ${JSON.stringify(products, null, 2)};
`;

fs.writeFileSync(OUT_PATH, content, "utf8");
console.log(`Wrote ${OUT_PATH} (${collections.length} collections, ${products.length} products).`);
